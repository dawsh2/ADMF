# src/core/exceptions.py

class ADMFTraderError(Exception):
    """Base class for exceptions in this application."""
    pass

class ConfigurationError(ADMFTraderError):
    """Exception raised for errors in the configuration."""
    pass

class ComponentError(ADMFTraderError):
    """Exception raised for errors related to components."""
    pass

class DependencyNotFoundError(ADMFTraderError):
    """Exception raised when a dependency cannot be resolved from the container."""
    pass

